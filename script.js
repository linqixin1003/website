function navigateWithLanguage(pageName) {
    try {
        if (!pageName) return;
        
        const currentLang = localStorage.getItem('selectedLanguage') || 'en';
        
        // ÂØπ‰∫éÂàÜÁ±ªÈ°µÈù¢ÔºåÂ¶ÇÊûú‰∏çÊòØËã±ÊñáÔºåÈúÄË¶ÅÊ£ÄÊü•ÊòØÂê¶Â∑≤ÁªèÂåÖÂê´ËØ≠Ë®ÄÂâçÁºÄ
        let newUrl = pageName;
        if (currentLang !== 'en' && !pageName.startsWith(currentLang + '/')) {
            // Â¶ÇÊûúÈ°µÈù¢ÂêçÁß∞‰∏çÂåÖÂê´ËØ≠Ë®ÄÂâçÁºÄÔºåÊ∑ªÂä†ËØ≠Ë®ÄÂèÇÊï∞
            const separator = pageName.includes('?') ? '&' : '?';
            newUrl = pageName + separator + 'lang=' + currentLang;
        }
        
        // Ë∑≥ËΩ¨Âà∞Êñ∞URL
        window.location.href = newUrl;
    } catch (error) {
        console.log('Navigation error:', error);
    }
}

