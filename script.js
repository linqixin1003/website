function translatePage(langCode) {
    if (!languages[langCode] || !languages[langCode].translations) {
        console.log('Translation not found for language:', langCode);
        return;
    }

    const translations = languages[langCode].translations;

    // ÁøªËØëÊâÄÊúâÂ∏¶Êúâdata-i18nÂ±ûÊÄßÁöÑÂÖÉÁ¥†
    const elementsToTranslate = document.querySelectorAll('[data-i18n]');
    
    elementsToTranslate.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[key]) {
            element.textContent = translations[key];
        }
    });

    // ÁøªËØëplaceholderÂ±ûÊÄß
    const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
    placeholderElements.forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[key]) {
            element.placeholder = translations[key];
        }
    });

    console.log('È°µÈù¢ÂÜÖÂÆπÂ∑≤ÁøªËØë‰∏∫:', languages[langCode].name);
}

