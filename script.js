function switchLanguage(langCode) {
    if (!languages[langCode]) {
        console.log('Language not found:', langCode);
        return;
    }
    
    currentLang = langCode;
    
    // ‰øùÂ≠òËØ≠Ë®ÄËÆæÁΩÆÂà∞ localStorage
    localStorage.setItem('selectedLanguage', langCode);
    
    // Êõ¥Êñ∞‰∏ãÊãâËèúÂçïÊòæÁ§∫
    const currentLangElement = document.getElementById('currentLang');
    if (currentLangElement) {
        currentLangElement.textContent = languages[langCode].code;
    }

    // Êõ¥Êñ∞ÈÄâ‰∏≠Áä∂ÊÄÅ
    const langOptions = document.querySelectorAll('.lang-option');
    langOptions.forEach(option => {
        option.classList.remove('active');
        if (option.innerHTML.includes(languages[langCode].code)) {
            option.classList.add('active');
        }
    });

    // ÂÖ≥Èó≠‰∏ãÊãâËèúÂçï
    const dropdown = document.querySelector('.lang-dropdown');
    if (dropdown) {
        dropdown.classList.remove('active');
    }

    // ÁøªËØëÈ°µÈù¢ÂÜÖÂÆπ
    translatePage(langCode);

    console.log('ÂàáÊç¢Âà∞ËØ≠Ë®Ä:', languages[langCode].name);
}

