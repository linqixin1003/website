function translatePage(langCode) {
    if (!languages[langCode]) {
        console.log('Language not found:', langCode);
        return;
    }
    
    // Â¶ÇÊûúÊ≤°ÊúâÁøªËØëÂÜÖÂÆπÔºå‰ΩøÁî®Ëã±ËØ≠‰Ωú‰∏∫Âü∫Á°Ä
    let translations = languages[langCode].translations || {};
    
    // ÂØπ‰∫éÈô§‰∏≠ÊñáÂíåËã±ÊñáÂ§ñÁöÑËØ≠Ë®ÄÔºå‰ΩøÁî®Ëã±ÊñáÂÜÖÂÆπ‰Ωú‰∏∫ÈªòËÆ§
    if (langCode !== 'en' && langCode !== 'zh' && Object.keys(translations).length === 0) {
        console.log('Using English as base for:', langCode);
        // ‰ΩøÁî®Ëã±ÊñáÂÜÖÂÆπÔºå‰ΩÜ‰øùÊåÅËØ≠Ë®ÄÊ†áËØÜ
        translations = languages['en'].translations || {};
    }

    // ÁøªËØëÊâÄÊúâÂ∏¶Êúâdata-i18nÂ±ûÊÄßÁöÑÂÖÉÁ¥†
    const elementsToTranslate = document.querySelectorAll('[data-i18n]');
    
    elementsToTranslate.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[key]) {
            element.textContent = translations[key];
        }
    });

    // ÁøªËØëplaceholderÂ±ûÊÄß
    const placeholderElements = document.querySelectorAll('[data-i18n-placeholder]');
    placeholderElements.forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[key]) {
            element.placeholder = translations[key];
        }
    });

    console.log('È°µÈù¢ÂÜÖÂÆπÂ∑≤ÁøªËØë‰∏∫:', languages[langCode].name);
}

